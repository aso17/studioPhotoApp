    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'categoryPhoto' => 'required',
            'description' => 'required',
            'priceCategory' => 'required',
            'exampleImage' => 'required|image|file|mimes:jpeg,png,jpg|max:2048'

        ]);

        $file = $request->file('exampleImage');
        $nama_file = $file->getClientOriginalName();
        $tujuan_upload = 'photo-category';
        $file->move($tujuan_upload, $nama_file);

        Category::create([
            'categoryPhoto' => $request->categoryPhoto,
            'description' => $request->description,
            'priceCategory' => $request->priceCategory,
            'exampleImage' => $nama_file
        ]);
        return redirect('/category')->with('success', 'create category successful');
    }

